<h3><center>Austin's Premier League Rules - Open Divisions</center></h3>

<h4>Player Dues</h4>
<p>
	<ol>
		<li>Player dues for all players for the season are $120.</li>
		<li>All dues must be paid in full by Week 1.</li>
		<li>Nonpayment of dues will result in being removed from the league, and could effect the player's ability to
			participate in future seasons.</li>
	</ol>
</p>

<h4>League Placement and Formation</h4>
<p>
    The open league is split into three different divisions: Division I, Division II, and Division III.  Players will be grouped into
    divisions by using the following process:
    <ul>
    	<li><b>Division I</b></li>
    		<ul>
    			<li>The top 5 finishers in Division I will automatically qualify to play in Division I for the following season.</li>
    			<li>The first place finisher in Division II will automatically move to Division I for the following season.</li>
    			<li>Any returning Division I players who did not earn an automatic spot in Division I, returning Division II players, and any new players that wish to
        			try for a spot in Division I can earn their spot through Division I Qualification Night.  More information on Division I Qualification Night is listed below.</li>
    		</ul>
    	<li><b>Division II</b></li>
    		<ul>
    			<li>The 2nd through 6th place finishers in Division II will automatically qualify to play in Division II for the following season.</li>
    			<li>The first place finisher in Division III will automatically move to Division II for the following season.</li>
    			<li>Any returning Division II players who did not earn an automatic spot in Division II, returning Division III players, and any new players that wish to
    				try for a spot in Division II can earn their spot through Division II Qualification Night.  More information on Division II Qualification Night is listed below.</li>
    		</ul>
    	<li><b>Division III</b></li>
    		<ul>
    			<li>Any player that does not qualify to play in Division I or Division II will have a spot in Division III.</li>
    			<li>New players to the league may request to begin in Division III.  These requests will be reviewed and approved by the league committee.</li>
    		</ul>
    </ul>
</p>

<h4>Division I Qualification Night</h4>
<p>
	Division I Qualification Night (DI Q-Night) will be held one week prior to the start of the season.  DI Q-Night is used to fill any open spots in Division I.
	  The format for DI Q-Night will be as follows:
	<ul>
		<li><b>8 or less players:</b></li>
			<ul>
				<li>One round-robin match against all other players.</li>
				<li>The players with the most number of legs won will fill spots in Division I as available.</li>
			</ul>
		<li><b>9 or more players:</b></li>
			<ul>
				<li>Players will be split into groups of 4 or 5.</li>
					<ul>
						<li>With 2 groups:</li>
							<ul>
								<li>One round-robin match against all other players in that group.</li>
								<li>The top 2 players from each group will advance to the final group.</li>
								<li>One wildcard (the next highest finisher) from the remaining players will advance to the final group.  In the event of a tie, both players
									will advance to the final group.</li>
								<li>The final group will play one round-robin match against all other players in that group.  The players with the most number of
									legs won in the final group will fill spots in Division I as available.</li>
							</ul>
						<li>With 3 or more groups:</li>
							<ul>
								<li>One round robin match against all other players in that group.</li>
								<li>The top 2 players from each group will advance to the final group.</li>
								<li>The final group will play one round-robin match against all other players in that group.  The players with the most number of
									legs won in the final group will fill spots in Division I as available.</li>
							</ul>
					</ul>
			</ul>
		<li><b>Match Format:</b></li>
			<ul>
				<li>Three games of 501.</li>
				<li>Players cork to start the match.  The winner of the cork will start legs #1 and #3.  The other player will start leg #2.</li>
				<li>All 3 games are played each match.</li>
			</ul>
	</ul>
</p>

<h4>Division II Qualification Night</h4>
<p>
	Division II Qualification Night (DII Q-Night) will be held during the week prior to the start of the season.  DII Q-Night is used to fill any open spots in Division II.
	  The format for DII Q-Night will be as follows:
	<ul>
		<li>One round-robin match against all other players.</li>
		<li>Match Format:</li>
			<ul>
				<li>Two games of 501.</li>
				<li>Players cork to begin each leg of the match with the closest player starting that leg.</li>
				<li>Both games are played each match.</li>
			</ul>
		<li>The players with the most number of legs won will fill spots in Division II as available.</li>
	</ul>
</p>

<h4>League Match Rules</h4>
<p>
	<ol>
		<li>All games are 501, straight in, double out.</li>
		<li>Division I matches are played "Best of 10".  First player to win 6 legs wins the match and no further games are necesary.
			Players can play to a 5-5 tie.</li>
		<li>Division II and Division III matches are played "Best of 8".  First player to win 5 legs wins the match and no further games are necesary.
			Players can play to a 4-4 tie.</li>
		<li>Players cork to start the match, with the player closest to bull starting the first leg, and all odd numbered
			legs after that.  The player furthest from the bull will start the even numbered legs.</li>
		<li>Both players are responsible for recording their stats from the match.</li>
	</ol>
</p>

<h4>Season Standings</h4>
<p>
	<ol>
		<li>All players will play every other player in the league in 3 matches throughout the course of the season.</li>
		<li>Points are awarded for the league standings as follows:</li>
		<p>
			<ul>
				<li>2 points for a match win</li>
				<li>1 point for a match tie</li>
				<li>0 points for a match loss</li>
			</ul>
		</p>
		<li>Total leg differential will be used to break any ties in the standings.</li>
		<li>If a player withdraws from the league, or is removed for non payment of dues, then all matches against that player
			(both played and unplayed) will be voided and no points will be awarded.</li>
	</ol>
</p>

<h4>Make-up Matches</h4>
<p>
	<ol>
		<li>In the event a player must miss a week, that player must notify:</li>
		<p>
			<ul>
				<li>The league coordinator</li>
				<li>Each of thier opponents for the week.</li>
			</ul>
		</p>
		<li>The player who will be absent is responsible for coordinating make-up matches with their opponents.</li>
		<li>The player who will be absent must pay the $5 bar contribution for the week they are absent.</li>
		<li>All make-up matches must be completed by the end of week 12.</li>
		<li>Players who have not completed their make up matches by the end of week 12 may be ineligible for tournament play.</li>
	</ol>
</p>
