<h3><center>Austin's Premier League Rules</center></h3>

<h4>Player Dues</h4>
<p>
	<ol>
		<li>Player dues for the season are $120.</li>
		<li>All dues must be paid in full by Week 3 (April 1).</li>
		<li>Nonpayment of dues will result in being removed from the league, and could effect the player's ability to
			participate in future seasons.</li>
	</ol>
</p>

<h4>Match Rules</h4>
<p>
	<ol>
		<li>All games are 501, straight in, double out.</li>
		<li>Each match is played "Best of 8".  First player to win 5 legs wins the match and no further games are necesary.
			Players can play to a 4-4 tie.</li>
		<li>Players cork to start the match, with the player closest to bull starting the first leg, and all odd numbered
			legs after that.  The player furthest from the bull will start the even numbered legs.</li>
		<li>Both players are responsible for recording their stats from the match.</li>
	</ol>
</p>

<h4>Season Standings</h4>
<p>
	<ol>
		<li>All players will play every other player in the league in 2 matches throughout the course of the season.</li>
		<li>Points are awarded for the league standings as follows:</li>
		<p>
			<ul>
				<li>2 points for a match win</li>
				<li>1 point for a match tie</li>
				<li>0 points for a match loss</li>
			</ul>
		</p>
		<li>Total leg differential will be used to break any ties in the standings.</li>
		<li>If a player withdraws from the league, or is removed for non payment of dues, then all matches against that player
			(both played and unplayed) will be voided and no points will be awarded.</li>
	</ol>
</p>

<h4>Make-up Matches</h4>
<p>
	<ol>
		<li>In the event a player must miss a week, that player must notify:</li>
		<p>
			<ul>
				<li>The league coordinator</li>
				<li>Each of thier opponents for the week.</li>
			</ul>
		</p>
		<li>The player who will be absent is responsible for coordinating make-up matches with their opponents.</li>
		<li>The player who will be absent must pay the $3 bar contribution for the week they are absent.</li>
		<li>All make-up matches must be complete by the end of week 11.</li>
		<li>Players who have not completed their make up matches by the end of week 11 may be ineligible for tournament play.</li>
	</ol>
</p>

<h4>Tournament Rules</h4>
<p>
	<ol>
		<li>The top 8 players from the league standings will qualify for the tourament.</li>
		<li>In the event a player who has qualified from the tournament is unable to participate, the player who finished
			9th in the standings will take their spot.  If the 9th place finisher is unable to play, or if more spots need to
			be filled, then spots in the tournament will be filled by the next highest league finisher as needed.</li>
		<li>All tournament games are 501, straight in, double out.</li>
		<li>The tournament will be seeded based upon finishing position in the league.  The matchups and format for the
			quarter-finals will be:</li>
		<p>
			<ul>
				<li>Match 1: #1 Seed vs #8 Seed</li>
				<li>Match 2: #4 Seed vs #5 Seed</li>
				<li>Match 3: #3 Seed vs #6 Seed</li>
				<li>Match 4: #2 Seed vs #7 Seed</li>
				<li>Best of 3 sets, each set is best of 3 legs.</li>
			</ul>
		</p>
		<li>The matchups and format for the semi-finals will be:</li>
		<p>
			<ul>
				<li>Match 5: Winner Match 1 vs Winner Match 2</li>
				<li>Match 6: Winner Match 3 vs Winner Match 4</li>
				<li>Best of 5 sets, each set is best of 3 legs.</li>
			</ul>
		</p>
		<li>The matchups and format for the consolation match will be:</li>
		<p>
			<ul>
				<li>Match 7: Loser Match 5 vs Loser Match 6</li>
				<li>Best of 5 sets, each set is best of 3 legs.</li>
				<li>The winner of the consolation match will be the third place finisher.</li>
			</ul>
		</p>
		<li>The matchups and format for the finals match will be:</li>
		<p>
			<ul>
				<li>Match 8: Winner Match 5 vs Winner Match 6</li>
				<li>Best of 5 sets, each set is best of 3 legs.</li>
				<li>The loser of the finals match will be the second place finisher.</li>
				<li>The winner of the finals match will be the first place finisher.</li>
			</ul>
		</p>
	</ol>
</p>

