<h3><center>Welcome to Austin's Premier League</center></h3>

<h4>About the Austin Premier League</h4>
<p>
    Established in 2013, the Austin Premier League is a singles dart league and is kicking off it's inagural season.  
    Currently, there is one division consisting of 12 players.  The goal of the league is to provide consistent and 
    challenging competition with a format to promote the development and growth of it's players.
</p>

<h4>League Format</h4>
<p>
    The Austin Premier league plays longer format 501 matches. The league season lasts for 12 weeks, including 11 
    weeks of round robin matches and culminates in a season ending tournament.  The league is played on Monday nights
    and each player plays 2 matches every week.  Each player will play every other player in the league in 2 matches
    throughout the course of the season.  The format for each match is as follows:
    <ul>
        <li>All games are 501.</li>
        <li>Each match is "Best of 8" games.  First to 5 wins, with 4-4 ties possible.</li>
        <li>Players cork to start the match, with the winner starting the first leg.  Alternating start for the
            remaining legs.</li>
    </ul>
    Scoring for league standings is as follows:
    <p>
    <ul>
        <li>2 points are awarded to the player who wins the match.</li>
        <li>1 point is awarded to both players in the event of a tie.</li>
        <li>0 points are awarded to the player who loses the match.</li>
        <li>Total leg differential is used to break ties in league standings.</li>
    </ul>
    </p>

    Individual statistics are tracked in the following categories:
    <p>
    <ul>
        <li>Number of darts thrown per leg.</li>
        <li>Number of scores of 93 or above.</li>
        <li>Number of 180's.</li>
    </ul>
    </p>
</p>

<h4>Tournament Format</h4>
<p>
    The top 8 players in the league standings at the end of season qualify for a seeded tournament.  The format
    for the tournament is as follows:
    <ul>
        <li>Quarter-final matches are best of 3 sets.  Each set is best of 3 legs.</li>
        <li>Semi-final matches are best of 5 sets.  Each set is best of 3 legs.</li>
        <li>The finals match is best of 5 sets.  Each set is best of 3 legs.</li>
    </ul>
</p>
